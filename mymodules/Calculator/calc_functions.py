def add(x,y):
    print(f"Performing addition of {(x,y)}")
    a = x + y
    return a
def sub(x,y):
    print(f"Performing subtraction of {(x,y)}")
    s = x - y
    return s
def mul(x,y):
    print(f"Performing multiplication of {(x,y)}")
    m = x * y
    return m
def div(x,y):
    print(f"Performing divisoin of {(x,y)}")
    d = x / y
    return d
def expo(x,y):
    print(f"Performing exponential of {(x,y)}")
    e = x ** y
    return e
def f_div(x,y):
    print(f"Performing floor division of {(x,y)}")
    fd = x // y
    return fd
def mod(x,y):
    print(f"Performing modulus of {(x,y)}")
    md = x % y
    return md